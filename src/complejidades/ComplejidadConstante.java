package complejidades;

public class ComplejidadConstante {
    /**
     * Complejidad: O(1)
     * Razón: Ejecuta siempre las mismas operaciones,
     * sin depender del tamaño de los datos.
     */
    public void ejemplo() {
        System.out.println("Ejemplo O(1):");
        int x = 10;
        int y = x + 5;
        System.out.println("Resultado: " + y);
    }
}
